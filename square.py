def area(a):
    """
    Calculate the area of a square.

    Parameters:
    a (float): The length of one side of the square.

    Returns:
    float: The area of the square.
    """
    return a * a

def perimeter(a):
    """
    Calculates the perimeter of a square.

    Parameters:
    a (float): The length of one side of the square.

    Returns:
    float: The perimeter of the square.
    """
    return 4 * a
